    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        int index = comboBox.getSelectedIndex();
        String selected = comboBox.getItemAt(index);
        String inputString = inputTextField.getText();
        Map<String, Integer> hm = frequencyOfWords(inputString);
        
        if (selected.equals("Đếm từ")) {
            int lengthOfInput = countWords(inputString);
            resultTextArea.setText(lengthOfInput + "");
        }
        
        if (selected.equals("Đếm từ trùng lặp")) {
            StringBuilder sb = new StringBuilder();
            
            hm.entrySet().forEach(entry -> {
                sb.append(entry.getKey()).append(": ").append(entry.getValue()).append("\n");
            });
            
            resultTextArea.setText(sb.toString());
        }
        
        if (selected.equals("Tiếng việt không dấu")) {
            resultTextArea.setText(removeAccent(inputString));
        }
    }//GEN-LAST:event_viewButtonActionPerformed
    
    private static int countWords(String s) {
        String[] sArray = s.split(" ");
        return sArray.length;
    }
    
    private static Map<String, Integer> frequencyOfWords(String s) {
        Map<String, Integer> hm = new HashMap<>();
        String[] sArray = s.split(" ");
        
        for (String i : sArray) {
            Integer j = hm.get(i);
            hm.put(i, (j == null) ? 1 : j + 1);
        }
        
        return hm;
    }
    
    private static String removeAccent(String s) {
        String result;
        
        String temp = Normalizer.normalize(s, Normalizer.Form.NFD);
        Pattern pattern = Pattern.compile("\\p{InCombiningDiacriticalMarks}+");
        result = pattern.matcher(temp).replaceAll("").replaceAll("đ", "d");
        
        return result;
    }

