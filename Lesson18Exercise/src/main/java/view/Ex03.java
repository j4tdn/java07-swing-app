    @Override
    public void run() {
        while (true) {
            Random rd = new Random();
            int number = rd.nextInt(7);

            switch (number) {
                case 0:
                    contentPane.setBackground(Color.RED);
                    colorLabel.setText("Random: RED");
                    break;
                case 1:
                    contentPane.setBackground(Color.GREEN);
                    colorLabel.setText("Random: GREEN");
                    break;
                case 2:
                    contentPane.setBackground(Color.BLUE);
                    colorLabel.setText("Random: BLUE");
                    break;
                case 3:
                    contentPane.setBackground(Color.YELLOW);
                    colorLabel.setText("Random: YELLOW");
                    break;
                case 4:
                    contentPane.setBackground(Color.PINK);
                    colorLabel.setText("Random: PINK");
                    break;
                case 5:
                    contentPane.setBackground(Color.BLACK);
                    colorLabel.setText("Random: BLACK");
                    break;
                case 6:
                    contentPane.setBackground(Color.WHITE);
                    colorLabel.setText("Random: WHITE");
                    break;
                default:
                    contentPane.setBackground(Color.RED);
                    colorLabel.setText("Random: RED");
            }

            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(Ex03.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

